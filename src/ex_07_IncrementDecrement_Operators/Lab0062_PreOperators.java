package ex_07_IncrementDecrement_Operators;

public class Lab0062_PreOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = ++a;//pre increment operator
        System.out.println(b);
        //In case of pre increment, value will first increase by 1 and it will assign.
        System.out.println(a);
        //++a means a = a+1;
        

    }
}
