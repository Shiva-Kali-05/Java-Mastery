package ex_04_Operators;

public class Lab0030_Unary_Operators {
    public static void main(String[] args) {
        //Unary operator
        int a = +10; //adding single + or - infront of value - unary operator
        int b = -20;
        int result = b+a;
        System.out.println(result);
        //Only one operand is working itself is called unary operator

    }
}
