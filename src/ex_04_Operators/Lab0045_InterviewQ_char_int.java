package ex_04_Operators;

public class Lab0045_InterviewQ_char_int {
    public static void main(String[] args) {
        char a = 'A';
        char b = 'B';
        System.out.println(a+b);
        // The ans is 131 because
        // ASCII - A value is 65
        // so B value is 66
        // 65+66 = 131
        System.out.println(a);
        // it will print A
        // when using + then char will become an integer



    }
}
