package ex_04_Operators;

public class Lab0041_All_Operators_Examples {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a+b);
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a!=b);
        // Compound operators
        int age = 24;
        age +=25; // it means age = age +24
        System.out.println(age);
        // in case of - minus
        int age1 =24;
        age1 -=25;
        System.out.println(age1);

        // Divide
        int age3 = 10;
        age3 /=2;
        System.out.println(age3);

    }
}
