package ex_10_For_Loop;

public class Lab0100_For_If_Loop {
    public static void main(String[] args) {
        for(int nihal =0;nihal<18;nihal++)
        {
            if(nihal>15)
            {
                System.out.println("will get gift");
            }
            else {
                System.out.println("no gift");
            }
        }
    }
}
